/**
  * @brief Calculate the moving average of samples in the avgBuffer
  * @retval uint8_t Moving average value
  */
uint8_t calculateMovingAverage(void)
{
  uint16_t sum = 0;
  uint8_t count = avgFilled > 0 ? avgFilled : 1;  // Avoid division by zero
  
  for (uint8_t i = 0; i < avgFilled; i++) {
    sum += avgBuffer[i];
  }
  
  return (uint8_t)(sum / count);
}

/**
  * @brief Detect if a sample is an outlier and replace it if necessary
  * @param sample The input sample to check
  * @retval uint8_t The filtered sample (original or replaced)
  */
uint8_t detectAndRejectOutlier(uint8_t sample)
{
  uint8_t avgValue = calculateMovingAverage();
  
  // Check if the sample deviates too much from the moving average
  if (abs((int)sample - (int)avgValue) > OUTLIER_THRESHOLD) {
    // Replace outlier with the last valid sample
    return lastValidSample;
  }
  
  // Not an outlier, update last valid sample
  lastValidSample = sample;
  return sample;
}

